function showToad(){
    var toad = document.getElementById("toad");
    var noButton = document.getElementById("noButton");


    toad.style.display = "block";
    noButton.style.display = "block";
}

function noToad(){
    var toad = document.getElementById("toad");
    var noButton = document.getElementById("noButton");

    toad.style.display = "none";
    noButton.style.display = "none";
}